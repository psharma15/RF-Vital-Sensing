function plotCute1(xLabel,yLabel,ax,plotTitle,plotLegend,showLegend,varargin)
%% PLOTCUTE1 takes 'x' and 'y' labels as input along with the axis, and 
% improves the plot with fixed settings        

fontSize = 10;

if nargin == 7
    legOrient = varargin{1};
else
    legOrient = 'Vertical'; % Default legend orientation
end

set(gca                       , ...
    'FontName'   , 'Helvetica', ...
    'FontSize'   , fontSize   ); % 8 for publication

if ~isempty(xLabel)
    hXLabel = xlabel(ax,xLabel);
    set(hXLabel, ...
    'FontName'   , 'Helvetica',...
    'FontSize'   , fontSize        ); % 8 for publication
else
    set(gca,'XTickLabels',[]);
end

if ~isempty(yLabel)
    hYLabel = ylabel(ax,yLabel); 
    set(hYLabel, ...
    'FontName'   , 'Helvetica',...
    'FontSize'   , fontSize        ); % 8 for publication
else
    set(ax,'yticklabel',[]) 
end

if showLegend
    hLegend = legend(plotLegend);
    set(hLegend,...
        'FontName'   , 'Helvetica',...
        'FontSize'   , fontSize   ,...
        'Orientation', legOrient  ); % 6 for publication
end

hTitle = title(ax,plotTitle);
set(hTitle, ...
    'FontName'   , 'Helvetica',...
    'FontSize'   , fontSize   ); % 8 for publication

set(ax, ...
  'Box'         , 'off'     , ...
  'TickDir'     , 'out'     , ...
  'TickLength'  , [.02 .02] , ...
  'XMinorTick'  , 'on'      , ...
  'YMinorTick'  , 'on'      , ...
  'YGrid'       , 'on'      , ...
  'XColor'      , [.3 .3 .3], ...
  'YColor'      , [.3 .3 .3], ...
  'LineWidth'   , 1         ); %'YTick'       , -0.008:0.002:0.008, ...

ax.XGrid = 'off';
ax.YGrid = 'off';

end